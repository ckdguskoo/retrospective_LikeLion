package day2;

public class arithmeticOperator {
    public static void main(String[] args) {
        //가독성을위해 a와 b만 사용하여 문제 해결
        //두 정수형 변수를 선언하고, 두 수의 합을 출력하세요.
        int a = 40;
        int b =4;
        System.out.println(a+b);

        //두 정수형 변수를 선언하고, 첫 번째 수에서
        // 두 번째 수를 뺀 결과를 출력하세요.
        System.out.println(a-b);

        //두 정수형 변수를 선언하고, 두 수의 곱을 출력하세요.
        System.out.println(a*b);

//      두 정수형 변수를 선언하고,
//      첫 번째 수를 두 번째 수로 나눈 결과를 출력하세요.
        System.out.println(a/b);

        //두 정수형 변수를 선언하고,
        // 첫 번째 수를 두 번째 수로 나눈 나머지를 출력하세요.
        System.out.println(a%b);

        int c = 10;

        System.out.println(++c);

        System.out.println(--c);






    }
}
